<h2 style="text-align:center;">General Information</h2>

<img src='static/L1157_slide_white_sm.png' style="float:right"></img>

<p>
  The TADPOL survey uses the 1mm dual-polarization system at CARMA to
  probe the morphology of magnetic fields in dense, star-forming
  cores.  Dust polarization maps with ~2'' angular resolution reveal
  whether the fields on scales of 0.005 pc are aligned with
  large-scale fields in the associated molecular-cloud envelopes, or
  with the orientations of bipolar outflows. The TADPOL collaboration
  includes members from all CARMA institutions (Berkeley, Caltech,
  Illinois, Maryland, and Chicago), as well as the University of
  Manitoba, the University of Western Ontario, the University of
  Arizona, the Herzberg Institute of Astrophysics, and the SOFIA
  Science Center.
</p>

